#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s = s + 1;
	}
	if ((char)c == '\0')
		return ((char *)s);
	else
		return (NULL);
}
